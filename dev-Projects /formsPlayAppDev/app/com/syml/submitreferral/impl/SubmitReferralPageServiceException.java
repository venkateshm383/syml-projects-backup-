package com.syml.submitreferral.impl;

public class SubmitReferralPageServiceException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -6965815395251293565L;

	public SubmitReferralPageServiceException() {
		super();
	}

	public SubmitReferralPageServiceException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SubmitReferralPageServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public SubmitReferralPageServiceException(String message) {
		super(message);
	}

	public SubmitReferralPageServiceException(Throwable cause) {
		super(cause);
	}

}
