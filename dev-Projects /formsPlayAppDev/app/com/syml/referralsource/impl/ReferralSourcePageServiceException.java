package com.syml.referralsource.impl;

public class ReferralSourcePageServiceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ReferralSourcePageServiceException() {
		super();
	}

	public ReferralSourcePageServiceException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ReferralSourcePageServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public ReferralSourcePageServiceException(String message) {
		super(message);
	}

	public ReferralSourcePageServiceException(Throwable cause) {
		super(cause);
	}
	
	

}
