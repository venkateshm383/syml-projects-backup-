	@SuppressWarnings("unchecked")
	public static List<Class<? extends BaseBean>> getDomainClassList(){
		Collection<String> classStrList = getDomainClasses();
		List<Class<? extends BaseBean>> classList = new ArrayList<Class<? extends BaseBean>>();
		for (String clazz : classStrList) {
			try {
				classList.add((Class<? extends BaseBean>) Class.forName(clazz));
			} catch (ClassNotFoundException e) {
				e.printStackTrace();
			}
		}
		return classList;
	}
	
